package A_Star_Algo;

public class GridParameters {
	private int length;
	private int breadth;
	private int xStart;
	private int yStart;
	private int xGoal;
	private int yGoal;
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getBreadth() {
		return breadth;
	}
	public void setBreadth(int breadth) {
		this.breadth = breadth;
	}
	public int getxStart() {
		return xStart;
	}
	public void setxStart(int xStart) {
		this.xStart = xStart;
	}
	public int getyStart() {
		return yStart;
	}
	public void setyStart(int yStart) {
		this.yStart = yStart;
	}
	public int getxGoal() {
		return xGoal;
	}
	public void setxGoal(int xGoal) {
		this.xGoal = xGoal;
	}
	public int getyGoal() {
		return yGoal;
	}
	public void setyGoal(int yGoal) {
		this.yGoal = yGoal;
	}
	
	
}
